usr_nama = input("masukkan nama anda: ")

if "adi" in usr_nama:
    print("halo adi, kamu adalah member VIP")
elif "mahmud" in usr_nama:
    print("Hai Mahmud, kamu adalah member Plus")
else:
    print("Hai kamu bukan member manapun :)")

    