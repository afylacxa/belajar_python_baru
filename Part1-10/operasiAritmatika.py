#operasi aritmatika

a = 12
b = 4

#operasi tambah +
hasil = a + b
print(a,"+",b ,"=",hasil)

#operasi kurang -
hasil = a - b
print(a,"-",b ,"=",hasil)

#operasi kali *
hasil = a * b
print(a,"*",b ,"=",hasil)

#operasi bagi /
hasil = a / b
print(a,"/",b ,"=",hasil)

#advance
#operasi eksponen (pangkat)
hasil = a ** b
print(a,"**",b ,"=",hasil)

#operasi modulus( bilangan Sisa dari pembagian) %
hasil = a % b
print(a,"%",b ,"=",hasil)

#operasi floor fivision // (membulatkan)
hasil = a // b
print(a,"//",b ,"=",hasil)


